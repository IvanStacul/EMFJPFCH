            cumplimiento a los objetivos y a las funciones conferidas a la
            Comisión Directiva en art. 21 inc. “h” del Estatuto vigente, entre
            otros, hemos tenido a bien emitir las siguientes:
          </p>

          <p>
            La
            <strong>Entidad De Magistrados Y Funcionarios De La Justicia De Paz Y
              Faltas De Chaco</strong>
            implementa un régimen de subsidios por Nacimiento y Adopción de
            hijos de asociados titulares que se regirá por las condiciones que
            se establecen en el presente Reglamento.
          </p>

          <h2>Normas</h2>

          <h3>ARTÍCULO PRIMERO</h3>
          <p>
            Será beneficiario del subsidio el asociado titular que acredite el
            nacimiento ó adopción de uno o más hijos, percibiendo tantos
            subsidios como hijos nacidos o adoptados declare. Se incluye
            asimismo los hijos de hijas solteras menores de edad de asociados
            titulares, en tanto y en cuanto sean inscriptos a su cargo en la
            Obra Social que corresponda.
          </p>

          <h3>ARTÍCULO SEGUNDO</h3>
          <p>
            Este subsidio tendrá vigencia cuando el asociado registre una
            antigüedad de afiliación superior a un (1) año y el nacimiento ó
            adopción se produzca con posterioridad a dicho plazo.
          </p>

          <h3>ARTÍCULO TERCERO</h3>
          <p>
            Para percibir el subsidio, el asociado titular deberá presentar a la
            Entidad la correspondiente solicitud y la partida de nacimiento ó
            sentencia de adopción.
          </p>

          <h3>ARTÍCULO CUARTO</h3>
          <p>
            En el supuesto que el asociado mantenga deuda con atraso en la
            Entidad, el monto del subsidio será aplicado hasta su concurrencia a
            la cancelación de la misma.
          </p>

          <h3>ARTÍCULO QUINTO</h3>
          <p>
            El importe del subsidio será fijado por la Comisión Directiva de
            acuerdo a la situación económico-financiera de la Entidad, y
            corresponderá abonarse el monto vigente a la fecha del nacimiento ó
            adopción.
          </p>

          <h3>ARTÍCULO SEXTO</h3>
          <p>
            El subsidio que no sea reclamado formalmente dentro de los SEIS (6)
            MESES de producido el nacimiento ó adopción, con la presentación de
            la pertinente documentación, perderá su vigencia caducando
            automáticamente, no siendo procedente reclamo de ninguna naturaleza
            en fecha posterior.
          </p>

          <h3>ARTÍCULO SÉPTIMO</h3>
          <p>
            Toda duda ó divergencia con respecto a la aplicación del presente
            Reglamento, será resuelta por la Comisión Directiva.
          </p>

