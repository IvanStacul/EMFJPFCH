      <article class="content_format row m-y-2">
        <div class="col-md-10 col-md-offset-1">
          <h1>
            Reglamento de Otorgamiento de Becas y Ayuda Economica para
            Capacitación
          </h1>

          <hr />

          <h2>Fundamentos</h2>

          <p>
            Que es necesario establecer normas claras que permitan a los
            integrantes de la Comisión Directiva de la Entidad de Magistrados y
            Funcionarios de la Justicia de Paz y Faltas de Chaco, administrar
            los recursos societarios aportados con base a criterios de
            legalidad, honestidad, eficiencia, eficacia, economía, racionalidad,
            austeridad, transparencia, control y rendición de cuentas;
          </p>

          <p>
            Que los gastos deberán ser los estrictamente necesarios para
            contribuir al eficaz y eficiente cumplimiento de los objetivos
            previstos en el art. 2 del Estatuto de la Entidad de Magistrados y
            funcionarios de la Justicia de Paz y Faltas de Chaco.
          </p>

          <p>
            Que las contribuciones económicas y/o becas aplicables a solventar
            costos realizados por los socios activos de esta Entidad, serán
            otorgados bajo criterios de austeridad, racionalidad, honestidad,
            eficiencia, eficacia, economía y transparencia, para dar
            cumplimiento a los objetivos y a las funciones conferidas a la
            Comisión Directiva en art. 21 inc. h del Estatuto vigente, entre
            otros, hemos tenido a bien emitir las siguientes:
          </p>

          <h2>Normas</h2>

          <h3>ART.1º:</h3>
          <p>
            El presente Reglamento regirá para todos los miembros de la Entidad
            de Magistrados y Funcionarios de la Justicia de Paz y Faltas de
            Chaco.
          </p>

          <h3>
            ART.2º: Del aporte económico, aplicable a capacitaciones y/o
            perfeccionamiento
          </h3>
          <p>
            Establecer que todos los socios activos de esta entidad tendrán
            derecho a solicitar un aporte económico, aplicable a capacitaciones
            y/o perfeccionamiento relativos a materias relacionadas directamente
            al ejercicio de la labor diariamente desempeñadas por los diversos
            Juzgado de Paz y Faltas, y de acuerdo al grado que su competencia le
            confiere.-
          </p>

          <h3>Art. 3: El beneficio</h3>
          <p>
            Se establece que una (1) vez al año, cada asociado, tiene derecho a
            percibir el subsidio por perfeccionamiento consistente en la suma
            actual de hasta pesos dos mil ($ 2.000). Los que serán destinado a
            la capacitación del beneficiario, a través de la realización de
            cursos, jornadas, diplomaturas, etc, que resulten de interés para el
            ejercicio de su labor, conforme lo prevé el art. 2 del presente
            Reglamento.-
          </p>

          <h3>Art. 4:</h3>
          <p>
            Los interesados en obtener el aporte, deben realizar una nota
            dirigida a la Comisión Directiva, solicitando dicho subsidio y
            especificando para que curso o jornada va a ser utilizado. Debiendo
            acreditar con posterioridad al curso, su participación mediante
            certificado, constancia, comprobante de pago, inscripción y/o
            asistencia, y/o cualquier otra documentación que acredite el pago y
            asistencia a las mismas.-
          </p>

          <h3>Art. 5.:De las Becas</h3>
          <p>
            Establecer que todos los socios activos de esta entidad tendrán
            derecho a solicitar ser beneficio de una beca anual, a fin de
            solventar gastos en la participación en Congreso y/o Jornada
            Nacional relativa a la Justicia de Paz y Faltas.-
          </p>

          <h3>Art. 6: Cronograma</h3>
          <p>
            Al Inicio del año calendario, los miembros de Comisión Directiva en
            reunión mensual determinarán, conforme al cronograma de Congresos
            y/o Jornadas Nacionales relativas a la Justicia de Paz y Faltas,
            cuáles serán los eventos en los que se reconocerá anualmente becas
            para los socios. Serán puestas a consideración y aprobación de
            Comisión Directiva, la que quedará aprobada y documentada en la
            respectiva acta de reunión del orden del día tratado.-
          </p>

          <h3>
            Art. 7: Criterio para la determinación del monto y beneficiario.
          </h3>

          <p>
            El monto de las mismas estará determinado por el valor de la
            inscripción (matrícula), más el 50% del costo del traslado (tomando
            como parámetro el precio del pasaje en colectivo).
          </p>

          <h3>Art. 8:</h3>
          <p>
            Se otorgará un total de veinte (20) becas por año y por evento
            fijado conforme art. 6. Los interesados en participar y obtener
            dicho beneficio, deberán poner en conocimiento de la Comisión
            Directiva que tienen interés en participar, y lo harán dentro de un
            plazo que va desde 30 días hasta 15 días antes, contados desde el
            día inmediato anterior a la fecha del evento.
          </p>

          <p>
            Si el número de socios interesados supera la cantidad de treinta
            (30) por evento, se procederá al sorteo de los cupos. A cada uno de
            los socios interesados inscriptos se le asignará un número de dos
            cifras, el que estará determinado por el orden alfabético de
            apellidos. La Comisión Directiva, a través de la Secretaria y/o
            encargado de relaciones públicas y comunicaciones, pondrá en
            conocimiento de todos los interesados el numero asignado, la fecha y
            modalidad del sorteo (el que será efectuado siempre de modo público,
            previa información a los interesados del lugar, fecha y hora en que
            se llevará a cabo, con la debida antelación).
          </p>

          <p>
            Los beneficiarios recibirán la beca, con suficiente antelación,
            quedando a su cargo la inscripción a la jornada que asistirá, y el
            depósito en efectivo para la misma. Debiendo acreditar
            documentadamente a Tesorería del monto asignado, a los efectos de la
            rendición mensual de Comisión Directiva.
          </p>

          <h3>Art. 9:</h3>
          <p>
            El encargado de relaciones públicas y comunicaciones designado de
            entre los integrantes de la Comisión Directiva, deberá publicitar
            y/o comunicar las fechas de los eventos previamente fijados (art.
            6), como así también la apertura y cierre de la inscripción, a la
            totalidad de los socios de la entidad, con el fin de garantizar un
            principio de igualdad de posibilidades de participación en el
            sorteo.-
          </p>

          <h3>Art. 10:</h3>
          <p>
            Las inscripciones se realizarán por correo electrónico o
            personalmente y de igual modo se les notificará la fecha del sorteo.
            En su caso se establecerá una lista de titulares y suplentes.
          </p>

          <h3>Art. 11:</h3>
          <p>
            En caso de que algún socio renuncie a la beca adjudicada, deberá
            informar de manera inmediata a la Comisión Directiva, como también
            reintegrarse el monto otorgado, para pasar el mismo al suplente que
            sigue en el orden correspondiente.
          </p>

          <h3>Art. 12:</h3>
          <p>
            Para el supuesto excepcional en Tesorería de la Entidad abone la
            matrícula y el beneficiario no concurra, con renuncia posterior, el
            descuento se hará del subsidio anual para perfeccionamiento que
            dispone cada socio, ya sea en año en curso o en el siguiente.
          </p>

